			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/Le_C%C5%93ur_d%27un_homme" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>

				<p>
					Mixé par Bob Clearmountain, Le Cœur d'un homme est réalisé par Yvan Cassar qui signe également les arrangements.

					L'album Amicalement blues d'Hubert-Félix Thiéfaine et Paul Personne paraît le même jour que Le Cœur d'un homme. L'idée de cette collaboration entre eux est directement liée à l'album de Johnny Hallyday : Thiéfaine et Personne furent sollicités pour écrire et composer des chansons en vue du prochain opus de Johnny, alors à l'état de projet. Plusieurs titres virent le jour, avant d'être finalement refusés par sa maison de disques. Thiéfaine et Personne décidèrent alors de poursuivre pour leur propre compte cette collaboration.<br><br>

					Johnny Hallyday dédie la chanson Que restera-t-il ? à sa mère Huguette Clerc, décédée le 29 août 2007.
				</p>
