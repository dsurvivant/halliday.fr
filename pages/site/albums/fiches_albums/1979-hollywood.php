					Le CD2 propose une version remixée de l'album avec les titres en « prises complètes » (d'une durée plus longue donc), ainsi qu'un titre inédit issue des sessions d'enregistrements de l'opus et une version alternative de C'est mieux ainsi (chanson parut en 45 tours en juin 19798).
