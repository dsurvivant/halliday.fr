			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/Derri%C3%A8re_l%27amour" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>

				<p>
					Sur les ondes, sur disques, sur scènes, Johnny Hallyday est omniprésent tout au long de l'année 1976. Cela commence un 13 février, jour où arrive dans les bacs des disquaires et aux radios, le 45 tours <em>Requiem pour un fou</em>, qui prend la tête de tous les hit-parade en quelques jours. Ça continue avec la chanson <em>Derrière l'amour</em>, qui sort un 9 juin et lui succède comme numéro un durant l'été. Le 23 juin, c'est <em>l'album Derrière</em> l'amour qui arrive chez les disquaires et qui deviendra sa plus grosse vente de la décennie. Puis la chanson <em>Gabrielle</em> débarque sur les ondes en septembre et est aussi un franc succès.
				</p>
				<p>
					1976, pour Johnny Hallyday c'est surtout son grand retour sur une scène parisienne, dont il est absent depuis cinq ans. Johnny se produit au Palais des sports de Paris du 28 septembre au 30 octobre, où il donne 35 représentations à guichets fermés, réunissant un total de 200 000 spectateurs (se faisant, il établit un nouveau record du plus grand nombre de spectateurs dans une salle parisienne pour un spectacle musical). Le double album <em>Johnny Hallyday Story - Palais des sports</em> est mis en vente le 20 octobre. Sitôt achevé ses concerts dans la capitale, Hallyday prend la route pour une tournée en province jusqu'à la fin de l'année (et au-delà).
				</p>
				<p>
					C'est dans ce contexte que sort le 7 novembre son premier double album studio Hamlet, un opéra rock adapté du classique de William Shakespeare. Johnny Hallyday est tellement omniprésent, sur disque, sur les ondes, sur scène, que son actualité fait de l'ombre à cet album dont il rêve depuis si longtemps - Hamlet est un projet vieux de six ans - et qu'à force de persévérance il est parvenu à concrétiser. La sortie du disque est discrète, personne ne l'attend, personne, pas même le principal intéressé ne l'annonce. Il n'est soutenu par aucune promotion, Si ce n'est une rencontre avec des lycéens au lycée Grandmont de Tours, le 3 février 1977, organisée et retransmise par RTL, comment l'emploi du temps le permettrait-il ? Le fait est que l'opus n'a guère de succès auprès du public et de la critique.
				</p>
				<p>
					De cet échec, Johnny dira plus tard :
				</p>
				<p>
					« ... J'avais demandé à Gilles Thibaut de travailler sur le projet [...] Il est revenu avec huit ou neuf titres tellement forts que je pensais en faire un spectacle mis en scène par Robert Hossein. L'année suivante, en 1970, j'ai travaillé le côté orchestral avec Jean-Claude Vannier. Ce n'est qu'en 1972 qu'on a enregistré, à Londres, les premières bases musicales [...] En 1976, Jacques Revaux est de l'aventure [...], la musique est de Pierre Groscolas [...], un orchestre de 150 musiciens [...] L'album sort dans l'indifférence générale en décalage complet. Trop tôt ou trop tard ? Dommage ce disque [...], j'y avais mis toutes mes tripes. »
				</p>
				<p>
					Le temps aidant, L'album, très marqué par le rock progressif, est devenu l'une des raretés de sa discographie et comme tel convoité et recherché par le public. Témoin, cette réédition CD des 40 albums studio de Johnny Hallyday, en 2000, où la semaine de sa sortie, Hamlet se place en tête des ventes dépassant même pour l'occasion Sang pour sang, (sorti en 1999, qui alors « explose » les ventes tout au long de l'année 2000, pour finalement deux millions d'exemplaires écoulés).
				</p>
				<p>
					La cause principale de l'échec commercial du disque n'est-elle pas qu'en 1976, le public n'avait guère d'engouement pour ce que l'on appelait alors un opéra-rock ? Précédemment, il y eut en 1973 l'opéra-rock La Révolution française de Claude-Michel Schönberg, Raymond Jeannot, Jean-Max Rivière et Alain Boublil. Il y aura encore, en 1979, Starmania, de Michel Berger et Luc Plamondon, deux créations originales.
				</p>
				<p>
					Il était novateur, en 1976, d'adapter musicalement un classique de la littérature et de le chanter, il n'y avait pas encore un public pour cela. En 1980, le roman Les Misérables est mis en musique par Claude-Michel Schönberg et Alain Boublil et l'œuvre éponyme est mise en scène au Palais des sports de Paris par Robert Hossein. Puis bien plus tard, en 1998, viendra le triomphe de l'adaptation, par Richard Cocciante et Luc Plamondon d'un autre classique de Victor Hugo, Notre dame de Paris, mais alors, les temps auront changé et les goûts du public aussi, on appelle désormais l'entreprise, non plus un opéra-rock mais une comédie musicale.
				</p>
				<p>
					Finalement l'échec de Johnny Hallyday n'est-ce pas d'avoir eu vingt ans d'avance sur le public et son époque ? C'est ce que souligne Jean-François Brieu lorsqu'il écrit :
				</p>
				<p>
					« L'absence d'extraits "vendeurs" pouvant séduire le public via les radios n'a laissé aucune chance à ce qui aurait pu être LE projet de la décennie soixante-dix [...] Aujourd'hui, le succès de Notre Dame de Paris montre à quel point Johnny Hallyday avait vu juste en s'attaquant à un texte classique pour en faire un spectacle moderne : il avait juste vingt ans d'avance. »
				</p>
