			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<P>Cet album est marqué par la première collaboration avec le journaliste écrivain et cinéaste <em>Philippe Labro.</em> On y retrouve également l'écrivain <em>Jacques Lanzmann</em> comme parolier.</P>
				<p>
				Ce disque est assez noir avec des thèmes qui touchent à la pollution, à la folie destructrice des hommes et à la trahison.<br>
				Johnny y chante un message d'appel à l'amour et à la fraternité (Essayez) à l'heure où la guerre du Vietnam fait rage.
				</p>
