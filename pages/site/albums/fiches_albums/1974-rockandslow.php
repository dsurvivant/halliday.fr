			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/Rock_%27n%27_Slow" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>
				<p>L'album, qui alterne adaptations et créations originales, marque le retour du chanteur au rock'n'roll. Johnny Hallyday chante Eddie Cochran, Chuck Berry et Elvis Presley et, pour la première fois de sa carrière, les Rolling Stones, avec Honky Tonk Women adapté en français par Long Chris. Johnny ne l'a jamais interprété sur scène, mais, par deux fois depuis, a inscrit l'original à son répertoire : à Bercy en 1990 et en 2006 au Palais des Sports (le soir de la première du spectacle, le guitariste du groupe, Ronnie Wood est présent dans la salle).</p>

				<p>Johnny Rider évoque, « façon western-road movie », les grands espaces américains, parcourus par le chanteur (en avril de cette même année), sur une Kawasaki 900, avec laquelle il traverse le Grand Canyon et la Vallée de la Mort.</p>

				<p>Les chansons sont écrites par Michel Mallory, Philippe Labro et Long Chris (il n'a plus fait de chansons pour Hallyday depuis 1969 et l'album Rivière… ouvre ton lit). Côté composition, on remarque la présence sur deux titres de Pierre Groscolas (futur compositeur de l'album Hamlet en 1976).</p>
