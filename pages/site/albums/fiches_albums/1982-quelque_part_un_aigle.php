			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				
				<p><a href="https://fr.wikipedia.org/wiki/Quelque_part_un_aigle" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>
				
				Quelque part un aigle est le premier album réalisé par Pierre Billon pour Johnny Hallyday qui renoue ici avec la composition et participe à l'écriture de l'ensemble des titres.<br><br>

				Véritable album « laboratoire » - on en compte un ou deux par décennie dans la discographie d'Hallyday : (précédemment) Rêve et amour en 1968, Hamlet en 1976 et (dans une moindre mesure), Hollywood en 1979 ; (et depuis) Ce que je sais en 1998, Jamais seul en 2011 - Quelque part un aigle explore des chemins encore inexplorés par l'artiste prenant le contre pied des thèmes qui sont les siens. Exit ici le « rockeur dur au cœur qui saigne », les « amours brisés ». Ici Johnny se paie même l'audace de « mettre en boîte » Hallyday, dans pas moins de quatre chansons : « T'es qu'un perdu qui se casse - t'est jamais qu'une mauvaise race » (La caisse), « Je serai sage ou du moins c'est ce que les gens penseront - mais au fond tout au fond - quelque part du font d'un trou quelque part du fond d'un lit - je préparerai en douce ma dernière grande connerie » (Sage pour vous), « Le doc m'a dit toc toc à 150 - ton cœur devrait se faire pote avec une ambulance - à force de battre à ce tempo il va te claquer dans la peau - un jour tu vas craquer moi ça ma fait marrer. » (Montpellier), « Quant sexuellement parlant j'suis un peu déprimé - les fantasmes en lambeaux et le corps épuisés - pas besoin d'infirmière toute nue sous sa blouse - quant je marche à côté de mes shoes j'me fais une cure de Blues » (Cure de Blues). Côté musique, le son des guitares se fait plus hard, plus agressif. Les cuivres - véritables marque de fabrique du son Hallyday depuis Le Pénitencier de 1964 - disparaissent, (mis à part çà et là quelques touches de saxophone), et cèdent la place aux claviers et synthétiseurs. Conséquence, (comme pour les précédents albums cités plus haut), le public en est quelque peu dérouté et le disque n'obtient qu'un succès relatif. Pas vraiment de tubes donc pour de cet album au concept original et son exploration d'univers musicaux différents.
