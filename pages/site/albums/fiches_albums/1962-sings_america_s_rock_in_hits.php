			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/Sings_America%27s_Rockin%27_Hits" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>

				<p>
					Du 17 au 20 février, Johnny Hallyday, pour la première fois à Nashville, au studio Bradley Recording Studio, enregistre en prise directe, seize chansons entièrement interprétées en anglais. À l'exception de l'adaptation anglaise de Retiens la nuit, il s'agit de reprises de standards américains. L'album, fait de rock 'n' roll et de ballades, propose douze titres. Les quatre autres sont diffusées sur divers 45 tours dans plusieurs pays ; en France, elles restent inédites durant près de trente années.<br><br>

					L'album n'étant pas uniquement destiné au public français (alors encore peu friand de chanson anglo-saxonne), sort dans plusieurs pays à travers le monde. Il témoigne de l'ambition d'Hallyday de percer sur le marché international, notamment aux États-Unis, où durant le premier semestre, Johnny effectue une tournée de promotion dans plusieurs villes, où il participe à de nombreuses émissions radiophoniques et chante à la télévision dans l'émission Ed Sullivan Show.<br><br>

					En France, la sortie de l'album est précédée par la diffusion, en mars, d'un super 45 tours nommé Johnny à New York, (Nashville évoque alors peu de choses au public français).<br><br>

					En mai, le chanteur revient à Nashville et enregistre seize autres chansons, dont la plupart resteront longtemps inédites.
				</p>
