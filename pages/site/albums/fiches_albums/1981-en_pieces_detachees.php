			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/En_pi%C3%A8ces_d%C3%A9tach%C3%A9es" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>
				<br>
				<p>
				L'album marque un net retour de Johnny Hallyday au rock. En pièces détachées à l'image de sa pochette en noir et blanc1, n'affiche que deux couleurs : grisaille et noirceur.</p>

				<p>Tout ici se chante en noir, point de place pour le bonheur, quant aux paradis, ils sont artificiels, l'amour se monnaye, les filles de Madame Lolita ne s'offrent pas elles se vendent. Ceux qui se sont aimés ne sont plus que Deux étrangers et ce qui reste de l'amour n'atteint jamais le dessus de la ceinture avec Cette fille-là, à qui on déclare Je peux te faire l'amour mais jamais au grand jamais, je t'aime.</p>

				<p>Mais En pièces détachées n'est pas seulement un album rock à vous filer le blues, il se veut aussi dérangeant.</p>

				<p>Johnny rockeur s'affirme chanteur français, le revendique, l'assume et, sans complexe, s'inscrit dans la lignée de ses homologues anglais et américains2 :</p>

				<p><blockquote>« Excusez-moi de ne pas être Américain, Anglais de Liverpool et même pas Canadien, Excusez-moi encore de chanter toujours du Rock'n'Roll, Y a pas que l'Amérique qui fait de la musique, C'est mon premier sentiment patriotique [...] J'suis un rockeur maudit c'est la haine d'être né à Paris »</blockquote></p>

				<p>Avec Monsieur Paul, c'est son aversion pour le monde de la politique et ces (autres) «  showmen », dont il ne craint pas vraiment la concurrence qu'il chante :</p>

				<p><blockquote>« [...] Tes sourires, tes discours, on n'y croit pas / On dirait du play back tellement c'est froid / Monsieur Paul tu peux gueuler t'aura jamais ma voix / J'voudrai jamais être un showmen comme toi [...] »</blockquote></p>

				<p>Avec Guerre (adaptation du titre Red, de Sammy Hagar, datant de 1977), Hallyday s'immisce dans l'univers du hard rock et au son des guitares saturées et de la batterie qui cogne, vocifère, tel un enragé, pour dénoncer les horreurs de la guerre et défendre « la colombe »3.</p>

				<p>Initialement prévue pour être nommée Lady Cocaïne, à la demande de la maison de disques, souhaitant que les angles soient quelques peu arrondis, la chanson est renommée Lady Divine</p>
