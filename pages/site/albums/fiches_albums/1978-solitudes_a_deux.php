		<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
		<div class="row well contenu">
			<p>Le titre de cet album reflète bien ce que Johnny ressentira toute sa carrière. Lui même exprimera dans diverses interviews, le sentiment de solitude au milieu de la multitude de personnes qui gravitent autour de lui.<br>
			La face 1 propose des ballades, et la face 2 des rythmes beaucoup plus dynamiques.<br><br>
					
			C'est la première participation de <em>Didier Barbelivien</em> parolier et musicien de "Elle m'oublie". Chanson qui sera le tube de l'été 1978 qui recevra le prix de la SACEM fin Aôut 1978. Didier Barbelivien écrira plus tard "Comme le soleil - 1979", "Du même côté de la rivière - 1979", "Monsieur Paul - 1981".<br><br>

			On remarquera également la participation <em>D'Eric Bamy</em>, célèbre choriste de Johnny, sur les titres "Va te cacher " et "Un coup pour rien".<br><br>

			2 Singles seront extraits de cet album (voir partie SINGLES).
			</p>
