			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/Pas_facile" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>

				<p>
					Enregistré à Paris au Studio du Palais des Congrès et au Studio de la Grande Armée, ainsi qu'à Boulogne Billancourt au Studio 92, l'album réalisé par Eddie Vartan reçoit un accueil honorable de la part du public et est rapidement certifié disque d'or. Pour autant, il ne se distingue pas particulièrement dans l'abondante discographie de l'artiste, il lui est souvent reproché un manque d'énergie, d'enthousiasme, ou plus généralement d'originalité, voir d'inspiration. La raison première en est l'absence de titres « forts ». Des dix chansons qui composent l'album aucune n'accèdent au rang de tube, et ce même si la chanson <strong>À partir de maintenant</strong> tourne sur les ondes durant tout l'été, précédant <strong>Un diable entouré d'anges</strong> qui assure la relève durant l'automne.
				</p>
				<p>
					L'opus contient essentiellement des ballades : <strong>Le Chanteur sans amour</strong> est dans la lignée de <strong>Né pour vivre sans amour</strong>, (album Derrière l'amour), et précède <strong>Le Chanteur abandonné</strong>, (album Rock'N'Roll Attitude). <strong>La Seule Fille que j'aime</strong> avec son long finale à la guitare acoustique est rondement mené ; quant à <strong>La Fille de l'hiver</strong>, pour quelques instants, à l'instar de sa semblable <strong>Il neige sur Nashville</strong> de l'été 1977, elle nous fait basculer de saison, alors que <strong>À double tour</strong> et <strong>Perdu dans le nombre</strong> assurent le service côté rock.
				</p>
				<p>
					C'est en fait par la reprise de <strong>La Poupée qui fait non</strong> de Michel Polnareff, et la création de <strong>Je ne suis pas un héros</strong>, écrite pour Johnny par Daniel Balavoine que s'impose avec le temps.
					Quelques jours avant la sortie de l'album, le 22 juin 1980, Johnny Hallyday commence sa tournée d'été par un concert à la Fête de la Liberté devant plus de 200 000 spectateurs. Ce soir là, il évoque l'exil de Michel Polnareff (qu'il ne nomme pas, se contentant de chanter un court extrait de <strong>La Poupée qui fait non</strong> reprit en chœurs par le public), souhaitant qu'il puisse bientôt revenir en France.<br>
					<strong>Je ne suis pas un héros</strong> passe totalement inaperçue, jusqu'à ce que Daniel Balavoine, sûr de son potentiel "tubesque", la reprenne à son compte et l'enregistre sur son album Un autre monde. Diffusée en simple, la chanson devient l'un de ses plus gros succès. Il la dédie par ailleurs à un certain Jean-Philippe Smet.
				</p>
				<p>
					Aucun titre de l'album ne fut repris sur scène, excepté <strong>Je ne suis pas un héros</strong>, que Johnny Hallyday inscrit à son tour de chant en 1990-1991 (voir Dans la chaleur de Bercy).
				</p>
