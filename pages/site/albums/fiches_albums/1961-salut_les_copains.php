			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p>
					1er album chez la maison de disque Philips.<br>
					Philips lui permet d'enregistrer à Londres avec des installations plus conséquents au studio Fontana, studio de Stanhope house.<br><br>
					Un album avec une présentation luxueuse. Pochette cartonnée avec sa tranche dorée. On y voit une photo , prise à l'Olympia, de Johnny pointant du doigt son public.<br>
					
					Il en sortire 3 45 tours EP (voir parti <a href="#singles">Singles</a>)
				</p>
