			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/La_Peur_(album)" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>
				<p>Réalisé spécialement à l'attention du spectacle Fantasmhallyday l'album sort quelques jours avant la rentrée de Johnny Hallyday au Palais des sports de Paris. Il est dans la mouvance de ce qu'est le show : en première partie présentation du "Survivant" ou l'apocalypse vu par Hallyday, puis en seconde partie un tour de chant « classique » donc plus rock'n'roll, dans la tradition des récitals de l'artiste. Fait selon le même concept, l'album propose en alternance les chansons du show (5 sur 11 - *) et d'autres, pour la plupart des rocks, plus proches des thèmes habituellement abordés par Johnny Hallyday.</p>
				<p>
				Les titres La peur et Il nous faudra parler d'amour un jour firent grandes impressions sur la scène du Palais des Sports ; Mais aussi Cartes postales d'Alabama (adaptation de Sweet Home Alabama de Lynyrd Skynyrd, adapté par Eddy Mitchell), Le Survivant (adaptation de You Ought To Know By Now de Ray Kennedy) et surtout Veau d'or vaudou qui étonne par la virulence de son texte (la chanson Je suis victime de l'amour est également au programme du Palais des Sports, en seconde partie). Je n'en suis plus capable propose un final à l'accordéon électronique tandis qu'Hallyday tout en voix sur Faire face, rivalise de puissance avec les cuivres.

				2 Singles seront extraits de cet album (voir partie SINGLES).
				</p>
