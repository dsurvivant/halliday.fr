				<p>
					<em>Philippe Labro</em> est le premier auteur à écrire tout un album pour Johnny Hallyday. Après Vie (précédent album du chanteur, auquel également collabore Labro), l'opus poursuit l'orientation de Johnny Hallyday vers des chansons évoquant des problèmes contemporains. Le disque, résolument rock, ne compte aucune ballade.
