			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/Derri%C3%A8re_l%27amour" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>
				<p>
				Derrière l'amour marque des changements artistiques importants dans la carrière de Johnny Hallyday, à commencer par l'arrivée de Jacques Revaux en tant que directeur artistique (les deux hommes ont déjà travaillé ensemble, entre 1966 et 1969, période durant laquelle, ils ont écrit quelques chansons). Sous sa houlette, la musique d'Hallyday se fait plus européenne et scelle la fin de la période londonienne du chanteur, où depuis 1966 il a enregistré à Londres l'essentiel de ses albums (avec toutefois nombre de sessions à Paris, mais aussi Los Angeles (1972), Rome (1974), et plus récemment en 1975, Memphis et Nashville).
				</p>
				<p>
				Enregistré à Boulogne-Billancourt au Studio 92, l'opus contient un grand nombre de créations originales, incisives, contemporaines, dites de « variétés », genre qu'elles renouvellent. Elles s'avèrent être de grands succès en devenir et permettent à Hallyday de toucher un public nouveau et d'augmenter en popularité.
				</p>
				<p>
				L'album éclectique, propose également son lot de rock : <strong>Joue pas de rock’n’roll pour moi</strong>, <strong>Gabrielle</strong>, <strong>L'étranger</strong>, <strong>Les Chiens de paille</strong>, <strong>Le jour J, l'heure H</strong>. Ce dernier, ainsi que <strong>Le Ghetto</strong>, écrit en 1966 et pressenti pour l'album La génération perdue, n'ont finalement pas été enregistrés et ont été oubliés durant dix ans.
				</p>
				<p>
				<strong>Joue pas de rock'n'roll pour moi</strong> s'imposera sur scène à diverses occasions, quant à <strong>Gabrielle</strong> depuis sa création à la scène, à l'automne 1976, au Palais des sports de Paris, elle n'a plus quitté le répertoire de son interprète. Avec <strong>Requiem pour un fou</strong> et <strong>Derrière l'amour</strong> les deux autres et premiers succès issus de l'album, elle s'inscrit parmi les standards de Johnny Hallyday.
				</p>
				<strong>Derrière l'amour</strong> est la plus grosse vente d'album de Johnny Hallyday aux cours des années 1970.
