			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p>
					<a href="https://fr.wikipedia.org/wiki/T%C3%AAte_%C3%A0_t%C3%AAte_avec_Johnny_Hallyday" target="_blank">
						<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
					</a>

					<p>
						Lorsque Vogue fait paraitre, le 31 juillet 1961, le 33 tours 25 cm Tête à tête avec Johnny Hallyday la rupture entre la maison de disque et le chanteur est déjà consommée (avec pour conséquence un procès qui trouvera son terme définitif en 1968 à la faveur du chanteur) et ce dernier est déjà, depuis le 19 juillet, sous contrat avec les disques Philips, qui font paraître leur premier 25cm d'Hallyday Viens danser le twist (enregistré du 5 au 12 septembre, en Angleterre aux studios Fontana), ainsi qu'un super 45 tours, le 20 septembre 1961, le jour même où pour la première fois il se produit à l'Olympia de Paris pour trente représentations.<br><br>

						Si (concernant le chanteur), il est la dernière parution originale de Vogue, le disque Tête à tête avec Johnny Hallyday ne propose aucune nouvelle chanson ; les huit morceaux au programme sont précédemment sortis en deux super 45 tours au printemps.<br><br>

						Pourtant, à posteriori, ce disque est dotée d'une rare version de la chanson À New Orléans d'une durée de 2'50 (les versions présentes sur le 45 tours promotionnel et sur le super 45 tours sont identiques). Toutes les rééditions postérieures à l'édition originale, ainsi que les compilations vinyles et/ou CD incluant la chanson, pour des raisons inconnues sont plus courtes de plusieurs secondes (le fondu en fin de morceau est différent), soit 2'40. Daniel Lesueur avance l'hypothèse que la bande magnétique originale a été égarée.
					</p>

				</p>
