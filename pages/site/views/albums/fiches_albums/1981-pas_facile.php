			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p><a href="https://fr.wikipedia.org/wiki/Pas_facile" target="_blank">
					<img alt="wikipedia" title="wikipedia" src="pages\site\images\logos\wikipedia.png">
				</a></p>
				<br>
				Le 4 novembre 1980, Sylvie Vartan et Johnny Hallyday se séparent officiellement. Un divorce qui met fin à 15 années d'une union passionnée autant que mouvementée.
				<br>
				Le 17 septembre 1981 sort l'album Pas facile et il ne parle que de cela, (ou presque), le dernier opus de Johnny, de cet amour qui n'est plus, de la solitude qu'il laisse. Les chansons s'enchaînent et ne s'adressent qu'à celle que jamais on ne nomme mais dont on devine la présence à chaque couplet. La solitude de l'artiste en est le concept et la principale inspiration.
				<br>
				La chanson <strong>C'est pas facile</strong> sur laquelle on chante avec force que tout va très bien mais que la nuit c'est autre chose… ouvre l'album et donne le ton. 
				<br>Avec <strong>Je t'ai aimée</strong>, Johnny offre une de ses plus grandes interprétations. 
				<br>Il ne faut pas me ressembler s'adresse à leur fils David. Chanson désespérée que Johnny Hallyday renie quelques années plus tard en déclarant : « Je l'ai chanté à une époque où je pensais avoir raté ma vie... aujourd'hui je vois les choses différemment ». 
				<br><strong>Avec Comme une femme</strong>, <strong>Toujours le même</strong> et <strong>Je ne pourrai jamais l'oublier</strong>, la litanie - tantôt inspirée tantôt moins - est toujours la même et la solitude hante chaque couplet.
				<br>
				On s'essaie bien au Rock - éternel refuge - pour le meilleur et le pire : Avec le méconnu <strong>Bats-toi pour l'amour</strong> et le moins inspiré <strong>Le Rock'n'Roll c'est comme ça</strong>.
				<br>
				<strong>Il n'y a plus de géant à l'Est d'Eden</strong> est une ballade empreinte de nostalgie qui rend hommage à James Dean et qui sonne ici comme une salutaire cure de jouvence.
				<br><strong> Avec J'en ai marre</strong>, retour au rock, sur lequel Hallyday règle des comptes avec une certaine presse « torchon », laquelle quelques mois plus tôt l'a déclaré mort d'un cancer de la gorge.
				<br>
				<strong>La ville</strong> vue par Hallyday ce sont les rues sombres et malfamées avec des lames de couteaux qui brillent. Johnny chante un univers où règne le chacun pour soi, qui histoire de finir dans la désespérance, conclut l'album.
				<br>
				Selon Frédéric Quinonero l'album - qui n'excède par le seuil des 80 000 exemplaires vendus - « reçoit un accueil médiocre » et l'auteur s'interroge sur le fait que, peut-être, « le public rejette l'image d'un Johnny loser ? »
