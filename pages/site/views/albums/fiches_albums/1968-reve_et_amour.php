			<h3 id="description"><span class="glyphicon glyphicon-info-sign"></span> Description <img class="chevron_bas" alt="chevron_bas" src="pages/site/images/boutons/chevron_bas.png"><img class="chevron_droite" alt="chevron_droite" src="pages/site/images/boutons/chevron_droite.png"></h3>
			<div class="row well contenu">
				<p>
					<em>Rêve et amour</em> est un album studio qui comme le précédent - <em>Jeune Homme</em> - réunit Johnny et son groupe, les New Blackburds. L'album passe assez inaperçu lors de sa sorite et laisse le public songeur devant tant de nouvelles inspirations.<em>Cours plus vite Charlie</em> adapté du <em>Cut Across Charly</em> d'Eddie Cochran, va pourtant se révéler un hit imparable.[..]<br>
					Johnny continue de travailler avec Long Chris et les News Blackburds. Leur influence est grande sur ce disque.[...]<br>
					Le 21 octobre 1968, l'album emplit les bacs. La pochette est assez Kitsch mais dans l'air du temps, inspirée de celle du Sergent Pepper's Hearts Club Band des Beatles. Elle représente Johnny en seigneur, vêtu d'un manteau royal, avec derrière lui son château fort. Son royaume est représenté par des petites vignettes sur lesquelles on s'amuse à reconnaître des visages célèbres. Le titre inscrit sur une bannière flottant au vent.<br>[...](source Collection Edition Atlas).
				</p>
			</div>

